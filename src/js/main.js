const board = document.querySelector('.board');
const rowTiles = 5;
const rowCount = 6;

let currentRow = 0;
let currentTile = 0;
const gameArr = [];
let dayWord = '';
let wordToCheck;

const popUp = document.querySelector('.popup');

const handleCreateTiles = () => {
	let rowNum = 0;
	for (let i = 0; i < rowCount; i++) {
		const newRow = document.createElement('div');
		newRow.classList.add('row');
		for (let i = 0; i < rowTiles; i++) {
			const newTile = document.createElement('div');
			newTile.classList.add('tile');
			newTile.set;
			newTile.setAttribute('id', `${rowNum}-${i}`);
			newRow.append(newTile);
		}
		board.appendChild(newRow);
		gameArr.push([]);
		rowNum++;
	}
};

const regex = /[A-Za-zżźćńółęąśŻŹĆĄŚĘŁÓŃ]/i;
let index = 0;

const handeKeyCheck = (e) => {
	if (index != 6) {
		if (e.key === 'Enter') {
			if (gameArr[index].length === 5) {
				wordToCheck = gameArr[index].reduce((some, some2) => some + some2);
				wordExist();
			} else {
				alert('Za mało liter');
			}
		}
		if (e.key.match(regex) && e.key.length < 2) {
			handleWriteLetter(e.key);
		}
		if (e.key === 'Backspace') {
			handleRemoveLetter();
		}
	} else {
		alert('przejebales kolego');
	}
};

const handleWriteLetter = (letter) => {
	if (gameArr[index].length < 5) {
		gameArr[index].push(letter);
		const activeTile = document.querySelectorAll('.tile');
		activeTile.forEach((tile) => {
			if (tile.getAttribute('id') === `${currentRow}-${currentTile}`) {
				tile.textContent = letter;
				tile.classList.add('show-animation');
			}
		});
	} else {
		return;
	}
	currentTile++;
};

const handleRemoveLetter = () => {
	if (gameArr[index].length > 0) {
		currentTile--;
		gameArr[index].pop();
		const activeTile = document.querySelectorAll('.tile');
		activeTile.forEach((tile) => {
			if (tile.getAttribute('id') === `${currentRow}-${currentTile}`) {
				tile.textContent = '';
				tile.classList.remove('show-animation');
			}
		});
	} else {
		return;
	}
};

const handleGame = () => {
	for (let i = 0; i < rowTiles; i++) {
		const tile = document.getElementById(`${currentRow}-${i}`);
		const currentLetter = tile.textContent;
		tile.classList.add('rotate');
		setTimeout(() => {
			if (currentLetter === dayWord[i]) {
				tile.style.backgroundColor = '#538D4E';
			} else if (dayWord.includes(currentLetter)) {
				tile.style.backgroundColor = '#B59F3B';
			} else {
				tile.style.backgroundColor = '#3A3A3C';
			}
		}, 300);
	}
	if (wordToCheck === dayWord) {
		setTimeout(() => {
			popUp.classList.add('active');
			popUp.textContent = 'Wygrałeś';
		}, 600);
		return;
	} else if (wordToCheck !== dayWord && index === 5) {
		popUp.classList.add('active');
		popUp.textContent = 'Przegrałeś';
		setTimeout(() => {
			location.reload();
		}, 2000);
		return;
	} else {
		index++;
		currentRow++;
		currentTile = 0;
	}
};

const wordExist = () => {
	if (words.includes(wordToCheck)) {
		handleConfirm();
	} else {
		popUp.classList.add('active');
		popUp.textContent = 'Nie ma takiego słowa';
		setTimeout(() => {
			popUp.classList.remove('active');
		}, 1500);
	}
};

const handleConfirm = () => {
	handleGame();
};

const handleDayWord = () => {
	const randomNumber = Math.floor(Math.random() * words.length);
	dayWord = words[randomNumber];
	console.log(dayWord);
};

const handleWebsiteLoaded = () => {
	handleCreateTiles();
	handleDayWord();
};

window.addEventListener('DOMContentLoaded', handleWebsiteLoaded);

document.addEventListener('keydown', handeKeyCheck);
